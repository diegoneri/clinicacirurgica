    class PressEnter implements KeyListener {

        @Override
        public void keyPressed(KeyEvent e) {
            // TODO Auto-generated method stub  

            if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                if (btnCLientes.isFocusOwner()) {
                    try {
                        chamarCliente();
                    } catch (ParseException ex) {
                        Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else if (btnProdutos.isFocusOwner()) {
                    executarGestaoProduto();

                } else if (btnUsuarios.isFocusOwner()) {
                    chamarUsuario();

                } else if (btnFornecedor.isFocusOwner()) {
                    try {
                        chamarFornecedor();
                    } catch (ParseException ex) {
                        Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else if (btnVendas.isFocusOwner()) {
                    executarGestaoVenda();
                } else if (btnContas.isFocusOwner()) {
                    try {
                        chamarDespesa();
                    } catch (ParseException ex) {
                        Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
                    }

                } else if (btnPedido.isFocusOwner()) {
                    executarGestaoPedido();
                } else if (btnNotas.isFocusOwner()) {
                    executarGestaoNotaFiscal();
                } else if (btnRelatorios.isFocusOwner()) {
                    try {
                        chamarRelatorio();
                    } catch (ParseException ex) {
                        Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (btnGraficos.isFocusOwner()) {
                    executarTelaGrafico();
                }
            } else if (e.getKeyCode() == KeyEvent.VK_F2) {
                executarCadastrarVenda();

            } else if (e.getKeyCode() == KeyEvent.VK_ESCAPE) {
                sair();
            }
        }

        @Override
        public void keyReleased(KeyEvent e) {
        }

        @Override
        public void keyTyped(KeyEvent e) {
        }
    }
