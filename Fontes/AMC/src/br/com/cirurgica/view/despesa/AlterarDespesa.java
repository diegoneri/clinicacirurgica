    public void limparCampos() {
        cbDataVencimento.setDate(new Date());
        cbDataReferente.setDate(new Date());
        txtValorDespesa.setText(null);
        ckDespesaPaga.setSelected(false);
        cbTipoDespesa.setSelectedIndex(0);

    }

    public void recuperarDespesa() {


        String sql = "select * from despesa d join tipodespesa td on(d.cd_tipo_despesa = td.cd_tipo_despesa) where d.cd_despesa = " + GestaoDespesa.cdDespesa;
        Connection con;
        con = CirurgicaConnectionManager.getConnection();
        Date dataVencimento;
        Date dataReferente;
        SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            if (rs.next()) {
                txtValorDespesa.setText(rs.getString("valor_despesa").replace(".",","));
                dataVencimento = new java.sql.Date(format.parse(rs.getString("data_vencimento")).getTime());
                dataReferente = new java.sql.Date(format.parse(rs.getString("data_referente")).getTime());

                cbDataVencimento.setDate(dataVencimento);
                cbDataReferente.setDate(dataReferente);


                if (rs.getString("isPaga").equals("1")) {
                    ckDespesaPaga.setSelected(true);
                }

                cbTipoDespesa.setSelectedItem(rs.getString("nm_tipo_despesa"));
                txtDescricao.setText(rs.getString("descricao"));
            }
        } catch (Exception e) {

            e.printStackTrace();
        }

    }

    public void popularComboTipoDespesa() {
        String sql = "select * from tipodespesa";
        Connection con;
        con = CirurgicaConnectionManager.getConnection();


        mapaTipoDespesa = new HashMap<>();

        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {

                mapaTipoDespesa.put(rs.getString("nm_tipo_despesa"), rs.getInt("cd_tipo_despesa"));
                cbTipoDespesa.addItem(rs.getString("nm_tipo_despesa"));
            }

            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

