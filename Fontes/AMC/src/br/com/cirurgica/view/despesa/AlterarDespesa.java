    public void alterarDespesa() {


       if (txtDescricao.getText().equals("") ||  txtValorDespesa.getText().equals("") || cbDataVencimento.getSelectedItem().equals(null) || cbDataReferente.getSelectedItem().equals(null)
                || cbTipoDespesa.getSelectedItem().equals(null)){

           JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de alterar uma Despesa!");

        }else{

        String sql = "update despesa set valor_despesa = ?, data_vencimento = ?, isPaga = ?, cd_tipo_despesa = ?, data_referente = ? , descricao = ?"
                + "where cd_despesa = '" + GestaoDespesa.cdDespesa + "'";

        Connection con = CirurgicaConnectionManager.getConnection();
        PreparedStatement stm;

        try {
            String dataVencimento = new SimpleDateFormat("yyyy-MM-dd").format(cbDataVencimento.getDate());
            String dataReferente = new SimpleDateFormat("yyyy-MM-dd").format(cbDataReferente.getDate());

            stm = con.prepareStatement(sql);
            stm.setString(1, txtValorDespesa.getText().replace(",", "."));
            stm.setString(2, dataVencimento);
            stm.setString(3, ckDespesaPaga.isSelected() ? "1" : "0");
            stm.setString(4, mapaTipoDespesa.get(cbTipoDespesa.getSelectedItem().toString()).toString());
            stm.setString(5, dataReferente);
            stm.setString(6, txtDescricao.getText());



            stm.executeUpdate();


            JOptionPane.showMessageDialog(this, "CADASTRO ALTERADO COM SUCESSO!!!", "Alteração", 1);


            this.setVisible(false);
            GestaoDespesa gd = new GestaoDespesa();
            gd.setVisible(true);

        } catch (Exception e) {

            e.printStackTrace();
        }

        }

    }

    public void voltar() throws ParseException {



            GestaoDespesa gd = new GestaoDespesa();
            gd.setVisible(true);
            this.dispose();

    }

    class PressEnter implements KeyListener {

        public void keyPressed(KeyEvent e) {
            // TODO Auto-generated method stub

            if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                if (btnAlterar.isFocusOwner()) {

                    alterarDespesa();

                } else if (btnCancelar.isFocusOwner()) {
                    try {
                        voltar();
                    } catch (ParseException ex) {
                        Logger.getLogger(CadastrarPedido.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (btnLimpar.isFocusOwner()) {
                    limparCampos();
                } else if (ckDespesaPaga.isFocusOwner()) {
                    if (ckDespesaPaga.isSelected() == true) {
                        ckDespesaPaga.setSelected(false);
                    } else {
                        ckDespesaPaga.setSelected(true);
                    }
                }
            } else if (e.getKeyCode() == KeyEvent.VK_ESCAPE) {
                try {
                    voltar();
                } catch (ParseException ex) {
                    Logger.getLogger(CadastrarPedido.class.getName()).log(Level.SEVERE, null, ex);
                }
            }


        }

        public void keyReleased(KeyEvent e) {
        }

        public void keyTyped(KeyEvent e) {
        }
    }
