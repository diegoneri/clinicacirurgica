    public void limparCampos() {
        cbDataVencimento.setDate(new Date());
        cbDataReferente.setDate(new Date());
        txtValorDespesa.setText(null);
        ckDespegaPaga.setSelected(false);
        cbTipoDespesa.setSelectedIndex(0);

    }

    class PressEnter implements KeyListener {

        public void keyPressed(KeyEvent e) {
            // TODO Auto-generated method stub

            if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                if (btnAdicionar.isFocusOwner()) {

                    cadastrarDespesa();

                } else if (btnCancelar.isFocusOwner()) {
                    try {
                        voltar();
                    } catch (ParseException ex) {
                        Logger.getLogger(CadastrarPedido.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (btnLimpar.isFocusOwner()) {
                    limparCampos();
                } else if (ckDespegaPaga.isFocusOwner()) {
                    if (ckDespegaPaga.isSelected() == true) {
                        ckDespegaPaga.setSelected(false);
                    } else {
                        ckDespegaPaga.setSelected(true);
                    }
                }
            } else if (e.getKeyCode() == KeyEvent.VK_ESCAPE) {
                try {
                    voltar();
                } catch (ParseException ex) {
                    Logger.getLogger(CadastrarPedido.class.getName()).log(Level.SEVERE, null, ex);
                }
            }


        }

        public void keyReleased(KeyEvent e) {
        }

        public void keyTyped(KeyEvent e) {
        }
    }
