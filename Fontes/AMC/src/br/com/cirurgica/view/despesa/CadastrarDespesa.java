    public void cadastrarDespesa() {

        if (txtDescricao.getText().equals("") || txtValorDespesa.getText().equals("") || cbDataVencimento.getSelectedItem().equals(null) || cbDataReferente.getSelectedItem().equals(null)
                || cbTipoDespesa.getSelectedItem().equals(null)){

           JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de Cadastrar uma Despesa!");

        }else{

        String sql = "insert into despesa(valor_despesa, data_vencimento, isPaga, cd_tipo_despesa, data_referente,descricao) "
                + "values(?,?,?,?,?,?);";


        Connection con = CirurgicaConnectionManager.getConnection();
        PreparedStatement stm;


        try {

            stm = con.prepareStatement(sql);
            String dataVencimento = new SimpleDateFormat("yyyy-MM-dd").format(cbDataVencimento.getDate());
            String dataReferente = new SimpleDateFormat("yyyy-MM-dd").format(cbDataReferente.getDate());

            stm.setString(1, txtValorDespesa.getText().replace(",", "."));
            stm.setString(2, dataVencimento);
            stm.setString(3, (ckDespegaPaga.isSelected() ? "1" : "0"));
            stm.setString(4, mapaTipoDespesa.get(cbTipoDespesa.getSelectedItem().toString()).toString());
            stm.setString(5, dataReferente);
            stm.setString(6, txtDescricao.getText());
            stm.executeUpdate();

            stm.close();
            con.close();

            JOptionPane.showMessageDialog(this, "CADASTRO EFETUADO COM SUCESSO!!!", "Cadastro", JOptionPane.INFORMATION_MESSAGE);



            this.setVisible(false);
            GestaoDespesa gd = new GestaoDespesa();
            gd.setVisible(true);


        } catch (Exception e) {

            e.printStackTrace();
        }
        }
    }

    public void voltar() throws ParseException {

        if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja cancelar o cadastro? (Pressione ESC para Cancelar)", "Confirmação", 2, 3) == 0) {


            GestaoDespesa gd = new GestaoDespesa();
            gd.setVisible(true);
            this.dispose();


        }
    }

    public void popularComboTipoDespesa() {
        String sql = "select * from tipodespesa";
        Connection con;
        con = CirurgicaConnectionManager.getConnection();


        mapaTipoDespesa = new HashMap<>();

        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {

                mapaTipoDespesa.put(rs.getString("nm_tipo_despesa"), rs.getInt("cd_tipo_despesa"));
                cbTipoDespesa.addItem(rs.getString("nm_tipo_despesa"));
            }

            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

