    public void excluirDespesa() throws ParseException {

        if (tbGestaoContas.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione uma Despesa para poder excluir.", "Sele√ß√£o Necess√°ria", 1);
        } else {
            if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este cadastro? (Pressione ESC para Cancelar)", "Confirma√ß√£o", 2, 3) == 0) {

                String sql = "delete from despesa where cd_despesa = " + listaCdDespesa.get(tbGestaoContas.getSelectedRow());

                Connection con = CirurgicaConnectionManager.getConnection();
                PreparedStatement stm;
                try {
                    stm = con.prepareStatement(sql);
                    stm.executeUpdate();

                    stm.close();
                    con.close();

                    JOptionPane.showMessageDialog(this, "CADASTRO EXCLUIDO COM SUCESSO!!!", "Remo√ß√£o", 1);

                } catch (SQLException ex) {
                    Logger.getLogger(GestaoCliente.class.getName()).log(Level.SEVERE, null, ex);
                }

                popularTabela();

            }

        }

    }

    public void popularTabela() throws ParseException {
        listaCdDespesa = new ArrayList<>();
        String sql = "select * from despesa d join tipodespesa td on(d.cd_tipo_despesa = td.cd_tipo_despesa) order by data_vencimento desc";
        Connection con;
        con = CirurgicaConnectionManager.getConnection();

        DefaultTableModel dataModel = (DefaultTableModel) tbGestaoContas.getModel();

        dataModel.setNumRows(0);

        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {
                Object[] linha = {"R$ " + rs.getString("valor_despesa").replace(".", ","), Utilidades.retornarDataFormatada(rs.getString("data_vencimento")), (rs.getString("isPaga").equals("0") ? "N√£o" : "Sim"), Utilidades.retornarDataFormatada(rs.getString("data_referente")), rs.getString("nm_tipo_despesa")};
                dataModel.addRow(linha);
                listaCdDespesa.add(rs.getString("cd_despesa"));
            }
            stmt.close();
        } catch (SQLException ex) {
            Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

