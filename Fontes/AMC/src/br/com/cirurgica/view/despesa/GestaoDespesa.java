    public void pesquisar() throws ParseException {

        listaCdDespesa = new ArrayList<>();
        String cdTipoDespesa = "";
        String sql = null;

        if (cbPesquisa.getSelectedItem().equals("")) {
            sql = "select * from despesa d join tipodespesa td on(d.cd_tipo_despesa = td.cd_tipo_despesa)";

        } else {
            cdTipoDespesa = mapaTipoDespesa.get(cbPesquisa.getSelectedItem()).toString();
            sql = "select * from despesa d join tipodespesa td on(d.cd_tipo_despesa = td.cd_tipo_despesa) where td.cd_tipo_despesa = " + cdTipoDespesa;


        }
        Connection con;
        con = CirurgicaConnectionManager.getConnection();



        DefaultTableModel dataModel = (DefaultTableModel) tbGestaoContas.getModel();

        dataModel.setNumRows(0);


        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {

                Object[] linha = {"R$ " + rs.getString("valor_despesa").replace(".", ","), Utilidades.retornarDataFormatada(rs.getString("data_vencimento")), (rs.getString("isPaga").equals("0") ? "N√£o" : "Sim"), Utilidades.retornarDataFormatada(rs.getString("data_referente")), rs.getString("nm_tipo_despesa")};


                dataModel.addRow(linha);

                listaCdDespesa.add(rs.getString("cd_despesa"));
            }

            stmt.close();

        } catch (SQLException ex) {
            Logger.getLogger(PrincipalView.class.getName()).log(Level.SEVERE, null, ex);
        }



    }

