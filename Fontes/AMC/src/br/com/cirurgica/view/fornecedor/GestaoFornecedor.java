    private void metodoPesquisar() {
        String nome = this.txtPesquisa.getText();

        Collection listaRetorno;
        if (nome.trim().equals("")) {
            listaRetorno = dao.findAll();
            bloquearNavegacao(false);
        } else {
            listaRetorno = dao.getAllByRazaoSocial(nome);
            bloquearNavegacao(true);
        }

        this.listaFornecedores.clear();
        if (listaRetorno != null && listaRetorno.size() > 0) {
            this.listaFornecedores.addAll(listaRetorno);
        } else {
            JOptionPane.showMessageDialog(this , "Não foram encontrados fornecedores com a Razão Social : " + nome , "Pesquisa de Fornecedores" , JOptionPane.WARNING_MESSAGE);
        }
    }

    private void bloquearNavegacao(boolean b) {
        this.btnPrimeiro.setEnabled(!b);
        this.btnAnterior.setEnabled(!b);
        this.btnProximo.setEnabled(!b);
        this.btnUltimo.setEnabled(!b);
    }

    class PressEnter implements KeyListener {

        @Override
        public void keyPressed(KeyEvent e) {
            // TODO Auto-generated method stub

            if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                if (btnAdicionar.isFocusOwner()) {
                    chamarCadastrarFornecedor();
                } else if (btnPesquisar.isFocusOwner()) {
                    metodoPesquisar();
                } else if (btnVoltar.isFocusOwner()) {
                    chamarVoltar();
                } else if (btnAlterar.isFocusOwner()) {
                    chamarVisualizarFornecedor();
                } else if (btnExcluir.isFocusOwner()) {
                    try {
                        excluirFornecedor();
                    } catch (ParseException ex) {
                        Logger.getLogger(GestaoCliente.class.getName()).log(Level.SEVERE , null , ex);
                    }
                } else if (txtPesquisa.isFocusOwner()) {
                    metodoPesquisar();
                }
            } else if (e.getKeyCode() == KeyEvent.VK_ESCAPE) {

                chamarVoltar();
            }
        }

        public void keyReleased(KeyEvent e) {
        }

        public void keyTyped(KeyEvent e) {
        }
    }

    public void chamarVoltar() {

        PrincipalView p = new PrincipalView();
        p.setVisible(true);
        p.setUsuarioLogado(Utilidades.retornaNomeLogado());
        this.dispose();

    }
