    public void excluirFornecedor() throws ParseException {
        if (tbGestaoFornecedores.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this , "Selecione um Fornecedor para poder excluir." , "Seleção Necessária" , 1);
        } else {
            if (JOptionPane.showConfirmDialog(this , "Tem certeza que deseja excluir este cadastro? (Pressione ESC para Cancelar)" , "Confirmação" , 2 , 3) == 0) {
                Connection con = CirurgicaConnectionManager.getConnection();
                PreparedStatement stm;

                String sql = "delete from fornecedor where CNPJ = '" + tbGestaoFornecedores.getModel().getValueAt(tbGestaoFornecedores.getSelectedRow() , 0).toString() + "'";


                try {
                    stm = con.prepareStatement(sql);
                    stm.executeUpdate();

                    stm.close();
                    con.close();


                    JOptionPane.showMessageDialog(this , "CADASTRO EXCLUIDO COM SUCESSO!!!" , "Remoção" , 1);

                } catch (SQLException ex) {
                    if (ex.getMessage().contains("Cannot delete or update a parent row")) {
                        JOptionPane.showMessageDialog(this , "Você não pode excluir este fornecedor. Existem produtos cadastrados que estão vinculados a ele." , "Erro de consistência de dados" , JOptionPane.ERROR_MESSAGE);
                    }
                    Logger.getLogger(this.getClass().getName()).log(Level.SEVERE , null , ex);
                }

            }


        }

        primeiraPagina();
    }

    public void chamarVisualizarFornecedor() {
        if (tbGestaoFornecedores.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this , "Selecione um Fornecedor para Visualizar e/ou Alterar seus dados." , "Seleção Necessária" , 1);
        } else {
            CPFAlteracao = tbGestaoFornecedores.getModel().getValueAt(tbGestaoFornecedores.getSelectedRow() , 0).toString();

            AlterarFornecedor af = new AlterarFornecedor();
            af.setVisible(true);
            this.dispose();


        }

    }

    private void primeiraPagina() {
        this.pagina = 1;
        this.popularTabela(pagina);
    }

    private void paginaAnterior() {
        if (--pagina <= 1) {
            pagina = 1;
        }
        this.popularTabela(pagina);
    }

    private void proximaPagina() {
        int ultimaPagina = dao.getLastPage().intValue();
        if (++pagina > ultimaPagina) {
            pagina = ultimaPagina;
        }
        this.popularTabela(pagina);
    }

    private void ultimaPagina() {
        this.pagina = dao.getLastPage().intValue();
        this.popularTabela(pagina);
    }

    private void popularTabela(int pagina) {
        this.listaFornecedores.clear();
        this.listaFornecedores.addAll(dao.findEntities(pagina));
    }

