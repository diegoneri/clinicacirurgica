    public void emitirRelatorio() throws JRException, SQLException, ParseException {
        Date d1 = (cbDataInicio.getDate() == null) ? new Date() : cbDataInicio.getDate();
        Date d2 = (cbDataFim.getDate() == null) ? new Date() : cbDataFim.getDate();

        if (buttonGroup.getSelection() != null) {
            RelatorioController rel = null;
            switch (buttonGroup.getSelection().getActionCommand()) {
                case "A":
                    //Analítico
                    rel = new RelatorioVendasController(d1, d2);
                    rel.imprimir();
                    break;
                case "S":
                    rel = new RelatorioVendasSinteticoController(d1, d2);
                    rel.imprimir();
                    break;
                default:
                    JOptionPane.showMessageDialog(this, "Favor selecionar um tipo de relatório");
                    return;
            }
        } else {
            JOptionPane.showMessageDialog(this, "Favor selecionar um tipo de relatório");
            return;
        }

    }

    public void voltar() {

        
        GestaoRelatorio gr = new GestaoRelatorio();
        gr.setVisible(true);
        this.dispose();

    }

    class PressEnter implements KeyListener {

        public void keyPressed(KeyEvent e) {
            // TODO Auto-generated method stub  

            if (e.getKeyCode() == KeyEvent.VK_ENTER) {
                if (btnEmitir.isFocusOwner()) {
                    try {
                        emitirRelatorio();
                    } catch (JRException ex) {
                        Logger.getLogger(GestaoRelatorio.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (SQLException ex) {
                        Logger.getLogger(GestaoRelatorio.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ParseException ex) {
                        Logger.getLogger(EmitirRelatorioVendas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (cbDataInicio.isFocusOwner()) {
                    try {
                        emitirRelatorio();
                    } catch (JRException ex) {
                        Logger.getLogger(EmitirRelatorioVendas.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (SQLException ex) {
                        Logger.getLogger(EmitirRelatorioVendas.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ParseException ex) {
                        Logger.getLogger(EmitirRelatorioVendas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (cbDataFim.isFocusOwner()) {
                    try {
                        emitirRelatorio();
                    } catch (JRException ex) {
                        Logger.getLogger(EmitirRelatorioVendas.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (SQLException ex) {
                        Logger.getLogger(EmitirRelatorioVendas.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (ParseException ex) {
                        Logger.getLogger(EmitirRelatorioVendas.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else if (btnVoltar.isFocusOwner()) {
                    voltar();
                }

            } else if (e.getKeyCode() == KeyEvent.VK_ESCAPE) {
                voltar();
            }
        }

        public void keyReleased(KeyEvent e) {
        }

        public void keyTyped(KeyEvent e) {
        }
    }
