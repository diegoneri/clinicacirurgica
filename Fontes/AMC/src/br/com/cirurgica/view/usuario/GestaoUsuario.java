    public void chamarVisualizarUsuario() {
        if (tbGestaoUsuario.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione um Usuário para Visualizar e/ou Alterar seus dados.", "Seleção Necessária", 1);
        } else {

            RGAlteracao = tbGestaoUsuario.getModel().getValueAt(tbGestaoUsuario.getSelectedRow(), 2).toString();

            AlterarUsuario au = new AlterarUsuario();
            au.setVisible(true);

            this.setVisible(false);

        }

    }

    public void chamarExcluirUsuario() {
        if (tbGestaoUsuario.getSelectedRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Selecione um Usuário para poder excluir.", "Seleção Necessária", 1);
        } else {
            if (JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este cadastro?", "Confirmação", 2, 3) == 0) {
                excluirUsuario();
            }
        }
        popularTabela();
    }

    public void chamarVoltar() {
        PrincipalView p = new PrincipalView();
        p.setVisible(true);
        p.setUsuarioLogado(Utilidades.retornaNomeLogado());
        this.dispose();
    }
