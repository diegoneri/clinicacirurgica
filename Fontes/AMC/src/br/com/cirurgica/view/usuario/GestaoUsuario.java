    public void pesquisarUsuario() {
        DefaultTableModel dataModel = (DefaultTableModel) tbGestaoUsuario.getModel();

        dataModel.setNumRows(0);
        String sql = "select *, max(v.data_venda) as ultima_venda from usuario u join pessoa p on(u.cd_pessoa = p.cd_pessoa) left join venda v on(u.cd_usuario = v.cd_usuario) where p.nm_pessoa like '" + txtPesquisa.getText() + "%' group by p.cd_pessoa";

        Connection con;
        con = CirurgicaConnectionManager.getConnection();


        try {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(sql);
            while (rs.next()) {

                Object[] linha = {rs.getString("nm_pessoa"), rs.getString("tipo_usuario"), rs.getString("RG"), Utilidades.retornarDataFormatada(rs.getString("ultima_venda"))};

                dataModel.addRow(linha);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }


    }

