    public void adicionarAoCarrinho() throws Exception {
        try {
            String cdProduto = txtProduto.getText();
            if (!cdProduto.trim().equals("")) {
                Produto p = produtoDAO.getByCodigoFarmaceutico(cdProduto);
                if (p != null) {
                    popularTabela(p);
                } else {
                    JOptionPane.showMessageDialog(this, "Produto nÃ£o encontrado!", "Busca de produto", JOptionPane.WARNING_MESSAGE);
                    txtProduto.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, "O produto nÃ£o foi adicionado a compra. Favor:"
                        + "\n1- Preencher o cÃ³digo do produto, ou"
                        + "\n2- Buscar por um produto", "Busca de produto", JOptionPane.WARNING_MESSAGE);
                txtProduto.requestFocus();
            }
        } catch (Exception ex) {
            Logger.getLogger(CadastrarVenda.class.getName()).log(Level.SEVERE, null, ex);
            throw ex;
        }
    }

    private void removerDoCarrinho() throws SQLException {
        this.vendaBean.removeItemVenda(this.tbCarrinho.getSelectedRow());
    }

    private void popularTabela(Produto p) throws Exception {
        VendaProduto vp = new VendaProduto();

        int quantidade = 0;
        try {
            quantidade = Integer.parseInt(txtQuantidade.getText().trim());
        } catch (NumberFormatException ex) {
            txtQuantidade.requestFocus();
            throw new Exception("Quantidade invÃ¡lida!", ex);
        }
        if (quantidade <=0 ) {
            throw new Exception("Quantidade deve ser igual ou maior a 1");
        }
        
        if (quantidade > p.getQtdeEstoque()) {
            throw new Exception("Estoque insuficiente! Estoque atual: " + p.getQtdeEstoque());
        }

        if (this.vendaBean.getNmTipoVenda().equals("Atacado")) {
            vp.setVlVendaProduto(p.getValorVendaAtacado());
        } else {
            vp.setVlVendaProduto(p.getValorVenda());
        }

        vp.setQtdeProduto(quantidade);

        VendaProdutoPK pk = new VendaProdutoPK();
        pk.setCdProduto(p);
        pk.setCdVenda(vendaBean);
        vp.setVendaProdutoPK(pk);

        this.vendaBean.addItemVenda(vp);
    }

